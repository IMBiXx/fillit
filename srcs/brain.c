#include "fillit.h"

void	place(t_piece *pieces)
{
	int		i;
	int		j;

	i = 0;
	while (pieces[i])
	{
		j = 0;
		while (j < 4)
			ft_putendl(pieces[i]->content[j++]);
		i++;
	}
}
