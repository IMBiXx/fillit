#include "fillit.h"


void	print_board(char **buff, int size)
{
	int		i;
	int		j;

	j = -1;
	while (++j < size)
	{
		i = -1;
		while (++i < size)
			ft_putchar(buff[j][i]);
		ft_putchar('\n');
	}
}
